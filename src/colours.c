#include "fractol.h"

int	encode_rgb(int t, int r, int g, int b, int endian)
{
	
	if (endian == 0)
	{
		return (b << 24 | g << 16 | r << 8 | t);
	}
	else
	{
		return (t << 24 | r << 16 | g << 8 | b);
	}
}
